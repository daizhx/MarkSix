    private void queryData(){
        String url = "http://52.77.216.6/api/marksix";
        JsonObjectRequest jsonObjectRequest = new JsonObjectRequest(url, new Response.Listener<JSONObject>() {
            @Override
            public void onResponse(JSONObject response) {
                Log.d("daizhx","1111111111");
                if(response != null){
                    try {
                        JSONArray array = (JSONArray) response.get("msg");
                        Log.d("daizhx","data="+array);
                        for(int i=0;i<array.length();i++){
                            JSONArray a = (JSONArray) array.get(i);
                            String[] ss = new String[3];
                            for (int j=0;j<a.length();j++){
                                String s = (String) a.get(j);
                                ss[j] = s;
                            }
                            dataSet.add(ss);
                        }
                        adapter.notifyDataSetChanged();
                    } catch (JSONException e) {
                        e.printStackTrace();
                    }
                }
            }
        }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {
                Log.d("daizhx","222222222"+error.getMessage());
            }
        });
        HttpUtils httpUtils = HttpUtils.getInstance(this);
        httpUtils.addToRequestQueue(jsonObjectRequest);
    }
